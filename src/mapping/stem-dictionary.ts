/**
 * Stem dictionary mapping stemmed words to domains with confidence weights.
 *
 * Each entry maps a Porter-stemmed word to one or more domains.
 * Weights range from 0.0 to 1.0:
 *   1.0 = strong signal (e.g., "financ" → finance)
 *   0.5 = moderate signal (e.g., "registr" → identifiers or transport)
 *   0.3 = weak signal (could be many things)
 *
 * The stems here are pre-computed outputs of our stemmer — they must match
 * what stem() actually produces, not theoretical Porter stems.
 */

export interface StemEntry {
  domain: string
  weight: number
}

